class PokerHand
  include Comparable
  ROYALS = [10, "Jack", "King", "Queen", "Ace"]
  def initialize(deck)
    @deck = deck
    @hand = get_hand
    @amount_count = Hash.new(0)
    @ranks = []
    @hand.each do |card|
      @ranks << card.rank
    end
    @ranks.each do |card|
      @amount_count[card] = @ranks.count(card)
    end
  end

  def get_hand
    holding = []
    5.times do 
      holding << @deck.draw
    end
    holding
  end

  def start_up_organize
    @hand.each do |card|
      @ranks << card.rank
    end
    @ranks.each do |card|
      @amount_count[card] = @ranks.count(card)
    end
  end

  def print
    @hand.each { |card| puts card }
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def n_count(number)
    @amount_count.values.include?(number)
  end

  def royal_flush?
     @hand.all? { |card| ROYALS.include?(card.rank)} && flush?
  end

  def straight_flush?
    @hand = @hand.sort
    straight? && flush?
  end

  def four_of_a_kind?
    n_count(4)
  end

  def full_house?
    @amount_count.values.include?(2) &&  @amount_count.values.include?(3)
  end

  def flush?
    suit_check = @hand[0].suit
    #binding.pry
    @hand.all? { |card| card.suit == suit_check}
  end

  def straight?
    return false if @amount_count.values.any? { |val| val > 1}
    @hand.min.value == @hand.max.value - 4
  end

  def three_of_a_kind?
    n_count(3)
  end

  def two_pair?
    @amount_count.values.count(2) == 2
  end

  def pair?
    n_count(2)
  end
end

